--------------------
--Tablas-- 
--------------------
-- Tabla: PUESTOS
CREATE TABLE PUESTOS (
    ID_PUESTO INT PRIMARY KEY,
    DESC_PUESTO VARCHAR2(100),
    SALARIO NUMBER
);

-- Tabla: EMPLEADOS
CREATE TABLE EMPLEADOS (
    ID_EMPLEADO INT PRIMARY KEY,
    ID_PUESTO INT,
    NOMBRE VARCHAR2(50),
    APELLIDO VARCHAR2(50),
    EDAD INT,
    EMAIL VARCHAR2(100),
    TELEFONO VARCHAR2(20),
    FECHA_CONTRATACION DATE,
    ESTATUS VARCHAR2(50)
);

-- Tabla: CLIENTES
CREATE TABLE CLIENTES (
    ID_CLIENTE INT PRIMARY KEY,
    NOMBRE VARCHAR2(50),
    APELLIDO VARCHAR2(50),
    DIRECCION VARCHAR2(100),
    TELEFONO VARCHAR2(20),
    EMAIL VARCHAR2(100)
);

-- Tabla: PROVEEDORES
CREATE TABLE PROVEEDORES (
    ID_PROVEEDOR INT PRIMARY KEY,
    NOMBRE VARCHAR2(50),
    DIRECCION VARCHAR2(100),
    TELEFONO VARCHAR2(20),
    EMAIL VARCHAR2(100)
);

-- Tabla: SERVICIOS
CREATE TABLE SERVICIOS (
    ID_SERVICIO INT PRIMARY KEY,
    DESC_SERVICIO VARCHAR2(100),
    PRECIO NUMBER
);

-- Tabla: MANTENIMIENTO
CREATE TABLE MANTENIMIENTO (
    ID_MANTENIMIENTO INT PRIMARY KEY,
    ID_SERVICIO INT,
    FECHA DATE,
    DIAGNOSTICO VARCHAR(200)
);

-- Tabla: AUTOS
CREATE TABLE AUTOS (
    ID_AUTO INT PRIMARY KEY,
    ID_PROVEEDOR INT,
    MODELO VARCHAR2(50),
    MARCA VARCHAR2(50),
    ANHO INT,
    PRECIO NUMBER,
    COLOR VARCHAR2(20),
    CILINDRADA INT,
    TIPO_GASOLINA VARCHAR2(20),
    KILOMETRAJE NUMBER,
    CANTIDAD INT,
    ID_MANTENIMIENTO INT,
    TIPO_TRANSMISION VARCHAR2(20),
    ESTADO VARCHAR2(50)
);

-- Tabla: FACTURAS
CREATE TABLE FACTURAS (
    ID_FACTURA INT PRIMARY KEY,
    SUB_TOTAL NUMBER,
    TOTAL NUMBER,
    FECHA DATE,
    HORA TIMESTAMP,
    ID_EMPLEADO INT
);

-- Tabla: VENTAS
CREATE TABLE VENTAS (
    ID_VENTA INT PRIMARY KEY,
    ID_FACTURA INT,
    FECHA DATE,
    ID_CLIENTE INT,
    ID_AUTO INT,
    ID_EMPLEADO INT
);

-- Tabla: REPUESTO
CREATE TABLE REPUESTO (
    ID_ITEM INT PRIMARY KEY,
    ID_AUTO INT,
    DESCRIPCION_REPUESTO VARCHAR2(100), 
    CANTIDAD INT
);

-- Tabla: COTIZACIONES
CREATE TABLE COTIZACIONES (
    ID_COTIZACION INT PRIMARY KEY,
    ID_CLIENTE INT,
    ID_AUTO INT,
    PRECIO_PROPUESTO NUMBER,
    FECHA DATE,
    ESTADO VARCHAR2(50)
);

-- Tabla: FINANCIAMIENTO
CREATE TABLE FINANCIAMIENTO (
    ID_FINANCIAMIENTO INT PRIMARY KEY,
    ID_CLIENTE INT,
    ID_AUTO INT,
    MONTO_PRESTAMO NUMBER,
    TASA_INTERES NUMBER,
    DURACION_MESES INT,
    FECHA_INICIO DATE,
    FECHA_FINAL DATE,
    ENTIDAD_BANCARIA VARCHAR2(50)
);




